function fecharPedido() {
  let texto_final = document.querySelector(".final span");
  texto_final.innerHTML = "Fechar pedido";
  let deixa_verde = document.querySelector(".final button");
  deixa_verde.classList.add("deixa_verde");
}

function verificaFecharPedido() {
  let elemento_p1 = document.querySelector(".p1").className;
  let elemento_p2 = document.querySelector(".p2").className;
  let elemento_p3 = document.querySelector(".p3").className;
  let elemento_p4 = document.querySelector(".p4").className;
  let elemento_b1 = document.querySelector(".b1").className;
  let elemento_b2 = document.querySelector(".b2").className;
  let elemento_b3 = document.querySelector(".b3").className;
  let elemento_b4 = document.querySelector(".b4").className;
  let elemento_s1 = document.querySelector(".s1").className;
  let elemento_s2 = document.querySelector(".s2").className;
  let elemento_s3 = document.querySelector(".s3").className;
  let elemento_s4 = document.querySelector(".s4").className;

  if (
    (elemento_p1[26] === "b") |
    (elemento_p2[26] === "b") |
    (elemento_p3[26] === "b") |
    (elemento_p4[26] === "b")
  ) {
    if (
      (elemento_b1[26] === "b") |
      (elemento_b2[26] === "b") |
      (elemento_b3[26] === "b") |
      (elemento_b4[26] === "b")
    ) {
      if (
        (elemento_s1[26] === "b") |
        (elemento_s2[26] === "b") |
        (elemento_s3[26] === "b") |
        (elemento_s4[26] === "b")
      ) {
        fecharPedido();
      }
    }
  }
}
